package edu.cuny.brooklyn.cisc3120.project.game;

public class Shot {
    private int x;
    private int y;
    
    public Shot(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
}
